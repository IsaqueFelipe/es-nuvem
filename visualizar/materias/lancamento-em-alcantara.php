	    <h3 class="pb-3 mb-4 font-italic border-bottom" style="text-align: center;">Lançamento do fogute (FTB) na Operação Falcão I/ 2020 obteve exito.</h3>

		<p>O Centro de Lançamento de Alcântara (CLA) realizou na tarde desta quinta-feira (25) o lançamento de um Foguete de Treinamento Básico (FTB) na Operação Falcão I/ 2020. A operação integra o cronograma de atividades de lançamento do CLA do ano de 2020. Além do treinamento operacional das equipes envolvidas, a Operação exercita as principais funções de comunicações, telemetria, rastreamento e gerenciamento dos procedimentos de segurança e comandos para lançamentos.</p>


		<p>O lançamento ocorreu às 17h52min (horário de Brasília) com o veículo atingindo uma altitude máxima (apogeu) superior de 30 quilômetros em 1min15seg. Em um total aproximado de 2min44seg de tempo em voo até o término do rastreio sobre a costa alcantarense.</p>

		<p>“Destaca-se na  Operação Falcão a operacionalidade do Centro, treinamento de equipes e meios, sendo essa operação voltada principalmente para a preparação para atividades de grande porte”, afirma o Diretor do CLA, Coronel Aviador Marcello Correa de Souza.</p>

		<p>A Operação iniciou no dia 22 de junho, seguindo com os procedimentos de segurança de interdição e esclarecimento de área marítima, patrulhamento terrestre e aviso aos navegantes para a comunidade pesqueira local.</p>

		<p>O Foguete de Treinamento Básico utiliza combustível sólido e é estabilizado por quatro hastes aerodinâmicas. O sistema de lançamento emprega um casulo reutilizável que pode ser novamente empregado em outras operações para integração do veículo junto à plataforma de lançamento.</p>

		<p>A Operação Falcão, com apoio da Agência Espacial Brasileira (AEB), é a primeira a ser realizada no CLA no ano de 2020 e, neste lançamento, não havia carga útil cientifica.</p>


