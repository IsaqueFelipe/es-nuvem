                            <h4>Porque Epaço Sideral?</h4>
                            <p>Segundo o dicionário Houaiss, a palavra "<u>espaço</u>" significa, <i><sup>1</sup> distância entre dois pontos <sup>2</sup> extensão limitada em uma duas ou três dimensões <sup>3</sup> extensão que compreende o sistema solar, as galáxias, as estrelas; o Universo.</i> E tambem segundo o dicionário Houaiss, a palavra "<u>sideral</u>" significa, <i><sup>1</sup>relativo a estrela ou a astros <sup>2</sup> relativo ou próprio do céu; celeste.</i></p>
                            <hr>

                            <?php include 'visualizar/anuncio-artigo-2.html';?>

