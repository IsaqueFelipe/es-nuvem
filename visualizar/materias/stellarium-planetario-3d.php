        <p>O programa conta diversas funções interativas para conhecer melhor o céu a partir da sua ou qualquer outra localização e tambem ferramentas para usuários mais avançados, dentre elas são:</p>
        <ul>
            <li>Simular e calcular o aumento de um telescópio com suas oculares.</li>
            <li>Controlar telescópios computadorizados remotamente via coordenadas.</li>
            <li>Calcular a posição de um astro em deterninada data e horário.</li>
            <li>Prever chuvas de meteoros e suas respectivas datas e locais no céu</li>
        </ul>
