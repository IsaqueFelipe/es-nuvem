        <p class="">Estellarium é um programa para plataformas desktop e mobile, muito interessante que simula um espaço de observação dos astros  em tempo real,
        e ainda te da todo o controle do tempo podendo o usuário voltar e adiantar o tempo a qualquer data escolhendo por exemplo a data de um eclipse que ocorreu no passado para rever de qualquer ponto do planeta, usando cordenadas  geograficas, nome da cidade ou sistema GPS.</p>
        <blockquote>
            <p>Nele você tambem tem um sistema de aconpanhameto de satélites em tempo real, alem da lista de satélies padrão que inclui a ISS(Estação espacial internacional) e o HST(Telescópio Hubble), ainda  você pode adicionar satélites manualmente a partir de outras listas disponiveis na internet.</p>
        </blockquote>
