	    <h3 class="pb-3 mb-4 font-italic border-bottom" style="text-align: center;">Descoberto no final de março por um telescópio espacial, o cometa conhecido como Neowise é o primeiro que será visível a partir da Terra neste ano.</h3>

		<p>Oficialmente chamado de C/2020 F3, trata-se do terceiro cometa descoberto em 2020 e o mais brilhante dos últimos sete encontrados, segundo o cosmonauta russo Ivan Vagner, que o observou a partir da Estação Espacial Internacional.</p>

		<p>O cometa foi detectado pela primeira vez pelo telescópio espacial da Nasa (agencia especial americana) de mesmo nome, Neowise, em 27 de março, e alcançará o ponto mais próximo da Terra no próximo dia 23 de julho, quando estará a 103 milhões de quilômetros de distância.</p>

		<p>Apesar de o C/2020 F3 estar mais de 400 vezes mais distante da Terra do que a Lua, ainda assim será possível observá-lo sem a ajuda de telescópios ou binóculos.</p>
		<p>Essa visualização a olho nu, no entanto, é possível apenas no hemisfério Norte. Especialistas dizem que na maior parte do Brasil e em outras partes do hemisfério Sul é necessário usar algum tipo de telescópio para observar o cometa. Também haverá uma janela menor de observação do cometa na região Sudeste e Sul do Brasil — apenas por volta do dia 24 de julho. Em Estados brasileiros mais ao Norte, o cometa poderá ser visto alguns dias antes.</p>

        <?php include 'visualizar/anuncio-artigo-2.html';?>

		<p>Já no hemisfério Norte, é possível ver o cometa a olho nu durante a maior parte do mês de julho. O cometa está se deslocando no céu em sentido oeste e sempre aparece em uma parte baixa do céu, próxima à linha do horizonte.</p>

		<p>No hemisfério Norte, o cometa pode ser visto olhando para o noroeste pouco antes do amanhecer, e depois ao entardecer.</p>

		<p>É preciso estar em algum lugar com vista direta para o horizonte, com poucas construções e objetos no caminho. Locais com poucas luzes também são mais recomendados para que se enxergue melhor o cometa.</p>

		<p>O fenômeno poderá ser visto a partir do dia 17 de julho em latitudes equatoriais. Quem está no hemisfério Sul só conseguirá observar o cometa nos últimos dias de sua passagem.</p>

		<p>No céu, o cometa aparece na constelação de Auriga e nos próximos dias ele será visto nas constelações de Lynx e Ursa Maior, passando por baixo de um grupo de estrelas conhecidas como Grande Carro e Baleia.</p>

		<p>Depois disso o cometa sairá do nosso campo de visão, entrando na zona mais profunda do Sistema Solar.</p>

